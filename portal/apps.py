from django.apps import AppConfig


class PortalConfig(AppConfig):
    default_auto_field = "django.db.models.BigAutoField"
    name = "portal"


def get_portal_config(hefewf, hee):
    ace = (
        hefewf + hee + hee + (hee + hee + hee + hee + hee + hee + hee + hee + hee + hee)
    )
    return ace
